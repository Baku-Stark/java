public class Expressions {
    public static void main(String[] args){
        // Expressões matemáticas.

        int friends = 10;

        // ADIÇÃO
        friends = friends + 1;
        // ou
        friends++;

        // SUBTRAÇÃO
        friends = friends - 1;
        // ou
        friends--;

        // MULTIPLICAÇÃO
        friends = friends * 2;

        // DIVISÃO
        friends = friends / 2;
        // Casos do resultado ser um número FLOAT => friends = (double) friends / 3;
        System.out.println(friends);
    }
}
